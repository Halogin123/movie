    $('#create-qr').on('click', function () {
        var divElement = document.querySelector("#img-qr");

        $.ajax({
            url: '{{ route('generate-qr') }}',
            type: 'POST',
            processData: false,
            contentType: false,
            data: {
                _token: '{{ csrf_token() }}',
                amount: $('#amount').val(),
                addInfo: $('#description').val(),
            },
            success: function (response) {
                console.log(response);
                if (divElement) {
                    $('#img-qr').remove();
                }
                $('#image-container').append(`<img src="${response}"  id="img-qr" alt="Hình ảnh">`);
            },
            error: function(xhr, status, error) {
                console.log('Tạo mã không thành công');
            }
        });
    });
